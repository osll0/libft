#include "libft.h"

int	ft_isspace(int c)
{
	unsigned char	uc;

	uc = (unsigned char)c;
	return (uc == ' ' || uc == '\t' || uc == '\n'
		|| uc == '\v' || uc == '\f' || uc == '\r');
}
